package bo.ucb.edu.ingsoft.model;

public class ProjectTags {
    private  Integer projectTagsId;
    private Integer TagsId;
    private Integer ProjectsId;

    public Integer getProjectTagsId() {
        return projectTagsId;
    }

    public void setProjectTagsId(Integer projectTagsId) {
        this.projectTagsId = projectTagsId;
    }

    public Integer getTagsId() {
        return TagsId;
    }

    public void setTagsId(Integer tagsId) {
        TagsId = tagsId;
    }

    public Integer getProjectsId() {
        return ProjectsId;
    }

    public void setProjectsId(Integer projectsId) {
        ProjectsId = projectsId;
    }
}
